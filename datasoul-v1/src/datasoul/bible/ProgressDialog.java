    public void isBibleDownload(boolean isDownload){
        btnCancel.setVisible(isDownload);
        if (isDownload){
            JobManager.addWorkListener(new MyWorkListener());
        }
    }

    public class MyWorkListener implements WorkListener{

        public void workProgressed(WorkEvent ev) {
            lblStatus.setText(ev.getJob().getSectionName()+"...");
            lblText.setText(ev.getJob().getJobName());
            if (ev.getJob().isFinished()){
                JobManager.removeWorkListener(this);
                dispose();
            }
        }

        public void workStateChanged(WorkEvent ev) {
        }
        
    }



