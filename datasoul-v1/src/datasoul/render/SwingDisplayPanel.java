    @Override
    public synchronized void paint (Graphics g){

        super.paint (g);
        
        synchronized(img){

            if (img != null && bgImg != null){
                if (isBlack){
                    g.setColor(Color.BLACK);
                    g.fillRect(0, 0, this.getWidth(), this.getHeight());
                }else{
                    g.drawImage(bgImg, 0,0, this.getWidth(), this.getHeight(), null);
                    if (!isClear){
                        g.drawImage(img, 0,0, this.getWidth(), this.getHeight(), null);
                    }
                }
            }else{
                System.out.println("Is null!");
            }
        }
    }
    
    public synchronized void clear(){

        synchronized(img){
            Graphics2D g = img.createGraphics();

            // Clear it first
            Composite oldComp = g.getComposite();
            try{
                g.setComposite( AlphaComposite.getInstance(AlphaComposite.CLEAR, 0) );
                g.fillRect(0, 0, img.getWidth(), img.getHeight());
            }finally{
                g.setComposite(oldComp);
            }
        }
    }
    
    public synchronized void flip(){
        // update the screen
        synchronized(img){
            this.repaint();
        }
        
    }
    
    
    public void initDisplay(int width, int height, int top, int left){
        this.setSize(width, height);
        img = new BufferedImage(width, height, BufferedImage.TYPE_4BYTE_ABGR);
        bgImg = new BufferedImage(width, height, BufferedImage.TYPE_4BYTE_ABGR);
        Graphics g = img.getGraphics();
        g.setColor(Color.ORANGE);
        g.fillRect(0, 0, width, height);
        this.setVisible(true);
    }
    
    
