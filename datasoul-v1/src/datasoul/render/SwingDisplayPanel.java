    @Override
    public void paint (Graphics g){

        super.paint (g);

        if (img != null){
            synchronized(img){
                g.drawImage(img, 0,0, this.getWidth(), this.getHeight(), null);
            }
        }else{
            System.out.println("Is null!");
        }
    }
    
