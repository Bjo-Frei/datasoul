    private void btnApplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApplyActionPerformed
        config.setSetX11Display( cbSetX11Display.isSelected() );
        config.setX11Display( txtX11Display.getText().trim() );
        config.setVideoDeintrelaceIdx(cbDeintrelace.getSelectedIndex());
        config.setVideoModeIdx(cbMode.getSelectedIndex());
        config.setVideoInputIdx(cbInput.getSelectedIndex());
        config.setDeviceName(txtDevice.getText());
        config.save();
        dispose();
    }//GEN-LAST:event_btnApplyActionPerformed

