    public void center(){
        Dimension screen = Toolkit.getDefaultToolkit().getScreenSize();
        Rectangle frame = getBounds();
        setLocation((screen.width - frame.width)/2, (screen.height - frame.height)/2);
    }
    
    public void showConfiguration(boolean isMonitor) {
        config = SDLLiveRenderConfig.getInstance(isMonitor);
        
        cbSetX11Display.setSelected (config.getSetX11Display());
        txtX11Display.setText( config.getX11Display() );
        
        txtDevice.setText( config.getDeviceName() );
        cbInput.setSelectedIndex( config.getVideoInputIdx() );
        cbDeintrelace.setSelectedIndex( config.getVideoDeintrelaceIdx() );
        cbMode.setSelectedIndex( config.getVideoModeIdx() );
       
        if (isMonitor){
            lblMonitor.setText(java.util.ResourceBundle.getBundle("datasoul/internationalize").getString("Monitor"));
        }else{
            lblMonitor.setText(java.util.ResourceBundle.getBundle("datasoul/internationalize").getString("Main"));
        }
        
        center();
        setVisible(true);
    }
    
